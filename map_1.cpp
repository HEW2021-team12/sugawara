//=============================================================================
// 初期化処理
//=============================================================================
void InitMap(void)
{
	g_Back = LoadTexture("data/TEXTURE/back.jpg");
	g_MapTexture = LoadTexture("data/MAP/MAP1.png");

	// メモリ確保
	g_map = new MAP[1];

	// マップUV情報
	g_map[0].u = 0.0f;
	g_map[0].v = 0.0f;
	g_map[0].uh = VIEW_SCREEN_WIDTH / MAP_X;
	g_map[0].vh = VIEW_SCREEN_HEIGHT / MAP_Y;
}

//=============================================================================
// 終了処理
//=============================================================================
void UninitMap(void)
{
	// メモリ解放
	delete g_map;
}

