void ChangeUv(void)
{
	float u, v;
	PLAYER* player = GetPlayer();

	u = player->pos.x / (MAP_X * CHIP_SIZE);
	v = player->pos.y / (MAP_Y * CHIP_SIZE);

	// U描画
	if (u < (g_map[0].uh / 2))
	{
		// 0.33fより小さいとき
		g_map[0].u = 0.0f;
		player->difference.x = -(((g_map[0].uh / 2) - u) * (MAP_X * CHIP_SIZE));
	}
	else if (u > (1.0f - (g_map[0].uh / 2)))
	{
		// 0.66fより大きいとき
		g_map[0].u = 1.0f - g_map[0].uh;
		player->difference.x = ((u - (1.0f - g_map[0].uh / 2)) * (MAP_X * CHIP_SIZE));
	}
	else
	{
		// それ以外
		g_map[0].u = u - (g_map[0].uh / 2);
		player->difference.x = 0.0f;
	}

	// V描画
	if (v < (g_map[0].vh / 2))
	{
		// 0.25fより小さいとき
		g_map[0].v = 0.0f;
		player->difference.y = -(((g_map[0].vh / 2) - v) * (MAP_Y * CHIP_SIZE));
	}
	else if (v > (1.0f - (g_map[0].vh / 2)))
	{
		// 0.75fより大きいとき
		g_map[0].v = 1.0f - g_map[0].vh;
		player->difference.y = ((v - (1.0f - g_map[0].vh / 2)) * (MAP_Y * CHIP_SIZE));
	}
	else
	{
		// それ以外
		g_map[0].v = v - (g_map[0].vh / 2);
		player->difference.y = 0.0f;
	}
}

int GetMapEnter(D3DXVECTOR2 pos)
{
	int gx = 0;
	int gy = 0;

	float sx, sy;

	// 横軸
	for (int x = 0; x < MAP_X; x++)
	{
		sx = CHIP_SIZE * x;

		if (pos.x < sx + CHIP_SIZE && // プレイヤー小、マップ大
			pos.x > sx)				  // プレイヤー大、マップ小
		{
			gx = x;
		}
	}

	// 縦軸
	for (int y = 0; y < MAP_Y; y++)
	{
		sy = CHIP_SIZE * y;

		if (pos.y < sy + CHIP_SIZE && // プレイヤー小、マップ大
			pos.y > sy)				  // プレイヤー大、マップ小
		{
			gy = y;
		}
	}

	return g_map_hitchk[gy][gx];
}

int GetMap(int x, int y)
{
	return g_map_hitchk[y][x];
}