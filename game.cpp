/*------------------------------------------------------------------------------
   グローバル変数の定義
------------------------------------------------------------------------------*/
static int g_BGMNo = 0;


/*------------------------------------------------------------------------------
   初期化関数
------------------------------------------------------------------------------*/
void InitGame(void)
{
	InitPlayer();
	InitEnemy();
	InitMap();
	InitGasGauge();
	InitTimer();

	g_BGMNo = LoadSound("data/BGM/BGM.wav");
	PlaySound(g_BGMNo, 255);
}

/*------------------------------------------------------------------------------
   終了処理をする関数
------------------------------------------------------------------------------*/
void UninitGame()
{
	StopSoundAll();

	//初期化とは逆順に終了処理を行う
	UninitMap();
	UninitPlayer();
	UninitEnemy();
	UninitGasGauge();
	UninitTimer();
}

/*------------------------------------------------------------------------------
   更新処理をする関数
------------------------------------------------------------------------------*/
void UpdateGame(void)
{
	UpdateMap();
	UpdateEnemy();
	UpdatePlayer();
	UpdateGasGauge();
	
	UpdateCollision();
	UpdateTimer();

	//スペースキーが押されていて、フェード処理中ではないとき
	if (GetKeyboardTrigger(DIK_RETURN) && GetFadeState() == FADE_NONE) 
	{
		
		//RESULTへ移行する
		SceneTransition(SCENE_RESULT);
	}
}

/*------------------------------------------------------------------------------
   描画処理をする関数
------------------------------------------------------------------------------*/
void DrawGame(void)
{
	DrawMap();
	DrawPlayer();
	DrawEnemy();
	DrawGasGauge();
	DrawTimer();
}