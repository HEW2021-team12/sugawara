//*****************************************************************************
// プロトタイプ宣言
//*****************************************************************************


//*****************************************************************************
// グローバル変数
//*****************************************************************************
unsigned char	g_GameoverTexture = 0;	// テクスチャ情報
unsigned char   g_GameoverBGM = 0;

//=============================================================================
// 初期化処理
//=============================================================================
HRESULT InitGameOver(void)
{
	g_GameoverTexture = LoadTexture("data/TEXTURE/over.png");
	g_GameoverBGM = LoadSound("data/BGM/LOSE.wav");

	PlaySound(g_GameoverBGM, 255);
	return S_OK;
}
//=============================================================================
// 終了処理
//=============================================================================
void UninitGameOver(void)
{
	StopSoundAll();
}
//=============================================================================
// 更新処理
//=============================================================================
void UpdateGameOver(void)
{
	if (Keyboard_IsKeyDown(KK_ENTER) ||
		IsButtonTriggered(0, XINPUT_GAMEPAD_A)
		&& GetFadeState() == FADE_NONE)
	{
		SceneTransition(SCENE_TITLE);
	}
}
//=============================================================================
// 描画処理
//=============================================================================
void DrawGameOver(void)
{
	DrawSpriteLeftTop(g_GameoverTexture, 0.0f, 0.0f, SCREEN_WIDTH, SCREEN_HEIGHT, 0.0f, 0.0f, 1.0f, 1.0f);
}
